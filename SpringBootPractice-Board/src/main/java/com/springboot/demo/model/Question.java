public class Question {
	private int qna_id;
	private String qna_user_id;
	private String qna_manager;
	private String qna_category;
	private String qna_question;
	private String qna_answer;

	public int getQna_id() {
		return qna_id;
	}

	public void setQna_id(int qna_id) {
		this.qna_id = qna_id;
	}

	public String getQna_user_id() {
		return qna_user_id;
	}

	public void setQna_user_id(String qna_user_id) {
		this.qna_user_id = qna_user_id;
	}

	public String getQna_manager() {
		return qna_manager;
	}

	public void setQna_manager(String qna_manager) {
		this.qna_manager = qna_manager;
	}

	public String getQna_category() {
		return qna_category;
	}

	public void setQna_category(String qna_category) {
		this.qna_category = qna_category;
	}

	public String getQna_question() {
		return qna_question;
	}

	public void setQna_question(String qna_question) {
		this.qna_question = qna_question;
	}

	public String getQna_answer() {
		return qna_answer;
	}

	public void setQna_answer(String qna_answer) {
		this.qna_answer = qna_answer;
	}

}
